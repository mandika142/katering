public void toExcel(JTable jTable1,File file){
        FileWriter excel = null;
        try {
            TableModel model = jTable1.getModel();
            excel = new FileWriter(file);
            for(int x=0;x<model.getColumnCount();x++){
                excel.write(model.getColumnName(x)+"\t");
            }   excel.write("\n");
            for(int x=0;x<model.getRowCount();x++){
                for(int y=0;y<model.getColumnCount();y++){
                    excel.write(model.getValueAt(x,y).toString()+"\t");
                }
                excel.write("\n");
            }   excel.close();
        } catch (IOException ex) {
            Logger.getLogger(laporan.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                excel.close();
            } catch (IOException ex) {
                Logger.getLogger(laporan.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    
}

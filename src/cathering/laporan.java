    private void jComboBox1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jComboBox1KeyPressed
        // TAMPILKAN LAPORAN BERDASARKAN KONDISI STATUS KEY PRESSED
        if (evt.getKeyCode()==KeyEvent.VK_ENTER){
            try {
            int row = jTable1.getRowCount();
            int row_i =1;
            for(int s=0;s<row;s++)
            {
                laporan.removeRow(0);
            }          
            Connection koneksi = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_cathering","root","");
            ResultSet rs = koneksi.createStatement().executeQuery("select *from pelanggan natural join pemesanan where status = '"+jComboBox1.getSelectedItem()+"' ");
            while(rs.next())
            {
                String barang[]={""+row_i++,rs.getString("id_pelanggan"),rs.getString("nama_pelanggan"),rs.getString("alamat_pelanggan"),
                    rs.getString("no_tlp"),rs.getString("tanggal_pengiriman"),rs.getString("paket_pesanan"),rs.getString("jumlah_pesanan"),
                rs.getString("total_harga"),rs.getString("dibayar"),rs.getString("sisa_pembayaran"),rs.getString("status")};
                laporan.addRow(barang);
            }
        } catch (SQLException ex) {
            Logger.getLogger(produk.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    }//GEN-LAST:event_jComboBox1KeyPressed

